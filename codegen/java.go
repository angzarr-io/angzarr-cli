// EmitScaffoldComponent writes the generate-once developer stub: a public class
// <Component> implementing the component's Handler interface.
func (e javaEmitter) EmitScaffoldComponent(g *protogen.GeneratedFile, file *protogen.File, s *Service) error {
	g.P("// Scaffolded ONCE by angzarr codegen java — this file is YOURS.")
	g.P("// Regeneration will NOT overwrite it; keep the generated Handler interface")
	g.P("// implemented as commands/events are added to the proto.")
	g.P()
	g.P("package ", javaPackage(file), ";")
	g.P()
	g.P("/** Implement ", s.GoName, "Handler here. */")
	g.P("public final class ", s.GoName, " implements ", s.GoName, "Angzarr.", s.GoName, "Handler {")
	for _, m := range e.methods(s) {
		g.P("  @Override public ", m.results, " ", m.name, "(", m.params, ")", m.throws, " {")
		if m.results == "void" {
			g.P("    // TODO: implement ", s.GoName, ".", m.name)
		} else {
			g.P("    throw new UnsupportedOperationException(", quote("TODO: implement "+s.GoName+"."+m.name), ");")
		}
		g.P("  }")
	}
	g.P("}")
	return nil
}

// --- Java naming helpers ------------------------------------------------------

func (a Applier) fqType() string { return string(a.Message.Desc.FullName()) }

func lowerFirst(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}

// parseAny renders the parse of an Any payload into the typed message, wrapping a
// decode failure as a coded ANY_DECODE_FAILED (a malformed payload is an invalid
// argument). Emitted inline as an immediately-invoked helper via a ternary is
// awkward in Java, so the parse is a guarded expression using a static helper on
// the message type plus a catch in a small lambda is overkill — instead the
// generated handler bodies wrap parse in a try/catch is verbose; we keep it
// simple: parseFrom throws and the trampoline codes it, but typed command/event
// parse failures should be ANY_DECODE_FAILED, so we route through CodedError.
func parseAny(m *protogen.Message, anyVar string) string {
	// io.angzarr.router.CodedError.parse(<Type>.parser(), anyVar) — a static
	// helper that maps InvalidProtocolBufferException to anyDecodeError.
	return jCoded + ".parse(" + javaType(m) + ".parser(), " + anyVar + ")"
}

// javaPackage resolves a file's Java package: the java_package option, else the
// proto package.
func javaPackage(file *protogen.File) string {
	fd := file.Desc
	if opts, ok := fd.Options().(*descriptorpb.FileOptions); ok && opts.JavaPackage != nil {
		return opts.GetJavaPackage()
	}
	return string(fd.Package())
}

// javaType computes the fully-qualified Java type name of a message: java
// package, then (unless java_multiple_files) the file's outer class, then the
// nested message path.
func javaType(m *protogen.Message) string {
	fd := m.Desc.ParentFile()
	opts, _ := fd.Options().(*descriptorpb.FileOptions)
	pkg := string(fd.Package())
	if opts != nil && opts.JavaPackage != nil {
		pkg = opts.GetJavaPackage()
	}
	nested := messageNestedName(m.Desc)
	if opts != nil && opts.GetJavaMultipleFiles() {
		return pkg + "." + nested
	}
	return pkg + "." + javaOuterClass(fd, opts) + "." + nested
}

// messageNestedName joins a message's name path within its file (Parent.Child).
func messageNestedName(md protoreflect.MessageDescriptor) string {
	parts := []string{string(md.Name())}
	for {
		parent, ok := md.Parent().(protoreflect.MessageDescriptor)
		if !ok {
			break
		}
		parts = append([]string{string(parent.Name())}, parts...)
		md = parent
	}
	return strings.Join(parts, ".")
}

// javaOuterClass derives the file's outer class name (java_outer_classname, else
// the PascalCase file base), applying protobuf's collision rule: if a top-level
// message/enum/service shares the name, append "OuterClass".
func javaOuterClass(fd protoreflect.FileDescriptor, opts *descriptorpb.FileOptions) string {
	if opts != nil && opts.JavaOuterClassname != nil {
		return opts.GetJavaOuterClassname()
	}
	base := path.Base(fd.Path())
	name := snakeToPascal(strings.TrimSuffix(base, ".proto"))
	if outerClassCollides(fd, name) {
		name += "OuterClass"
	}
	return name
}

func outerClassCollides(fd protoreflect.FileDescriptor, name string) bool {
	msgs := fd.Messages()
	for i := 0; i < msgs.Len(); i++ {
		if string(msgs.Get(i).Name()) == name {
			return true
		}
	}
	enums := fd.Enums()
	for i := 0; i < enums.Len(); i++ {
		if string(enums.Get(i).Name()) == name {
			return true
		}
	}
	svcs := fd.Services()
	for i := 0; i < svcs.Len(); i++ {
		if string(svcs.Get(i).Name()) == name {
			return true
		}
	}
	return false
}

func snakeToPascal(s string) string {
	var b strings.Builder
	upper := true
	for _, r := range s {
		if r == '_' {
			upper = true
			continue
		}
		if upper {
			b.WriteString(strings.ToUpper(string(r)))
			upper = false
		} else {
			b.WriteRune(r)
		}
	}
	return b.String()
}
