package codegen

// Java emitter. Java forbids multiple public top-level classes per file, and the
// conformance fixtures (a separate package) must import the handler types, so —
// unlike the Go/Python emitters' one-public-class-per-component shape — each
// proto file's components are emitted into ONE wiring file as a single public
// outer class (named <protofile>_angzarr) holding a public nested
// <Component>Handler interface, a new<Component>Dispatch factory, and a
// register<Component> convenience per component. Type references are emitted as
// fully-qualified names (no import management): framework/runtime types under
// io.angzarr / io.angzarr.router, and domain message types computed from the
// file's java options. The envelope stays google.protobuf.Any end to end; state
// is a protobuf Builder (protobuf-java is immutable) that appliers fold into.

import (
	"fmt"
	"path"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/reflect/protoreflect"
	"google.golang.org/protobuf/types/descriptorpb"
)

// Runtime (binding) + framework (message) type names, fully qualified.
const (
	jRouter       = "io.angzarr.router.Router"
	jAggDispatch  = "io.angzarr.router.AggregateDispatch"
	jSagaDispatch = "io.angzarr.router.SagaDispatch"
	jProjDispatch = "io.angzarr.router.ProjectorDispatch"
	jPmDispatch   = "io.angzarr.router.ProcessManagerDispatch"
	jRebuilder    = "io.angzarr.router.Rebuilder"
	jCctx         = "io.angzarr.router.CommandContext"
	jDestinations = "io.angzarr.router.Destinations"
	jPack         = "io.angzarr.router.Pack"
	jCoded        = "io.angzarr.router.CodedError"
	jSagaEmission = "io.angzarr.router.Thunks.SagaEmission"
	jPmRejection  = "io.angzarr.router.Thunks.PmRejection"
	jEventBook    = "io.angzarr.EventBook"
	jEventPage    = "io.angzarr.EventPage"
	jBusinessResp = "io.angzarr.BusinessResponse"
	jNotification = "io.angzarr.Notification"
	jRejNotif     = "io.angzarr.RejectionNotification"
	jProjection   = "io.angzarr.Projection"
	jPmResponse   = "io.angzarr.ProcessManagerHandleResponse"
	jAny          = "com.google.protobuf.Any"
	jList         = "java.util.List"
)

type javaEmitter struct{}

func (javaEmitter) Lang() string { return "java" }

// Java forbids multiple public top-level types per file, so each component is a
// public final class <Component>Angzarr (matching the filename) holding the
// nested <Component>Handler interface + static new/register methods.
func (javaEmitter) WiringPath(file *protogen.File, s *Service) string {
	return componentFile(file, s.GoName+"Angzarr", ".java")
}

func (javaEmitter) ScaffoldPath(file *protogen.File, s *Service) string {
	return componentFile(file, s.GoName, ".java")
}

func (e javaEmitter) EmitComponent(g *protogen.GeneratedFile, file *protogen.File, s *Service) error {
	g.P("// Code generated by angzarr codegen java. DO NOT EDIT.")
	g.P("// source: ", file.Desc.Path())
	g.P()
	g.P("package ", javaPackage(file), ";")
	g.P()
	g.P("@SuppressWarnings({\"unchecked\", \"all\"})")
	cls := s.GoName + "Angzarr"
	g.P("public final class ", cls, " {")
	g.P("  private ", cls, "() {}")
	g.P()
	var err error
	switch s.Component.Kind {
	case KindAggregate:
		err = e.emitAggregate(g, s)
	case KindSaga:
		err = e.emitSaga(g, s)
	case KindProjector:
		err = e.emitProjector(g, s)
	case KindProcessManager:
		err = e.emitPM(g, s)
	default:
		err = fmt.Errorf("unsupported component kind %v", s.Component.Kind)
	}
	if err != nil {
		return err
	}
	g.P("}")
	return nil
}

// --- interface (the strict seam) ----------------------------------------------

func (e javaEmitter) emitInterface(g *protogen.GeneratedFile, s *Service, doc string) {
	g.P("  /** The strict business seam for the ", doc, ". Every declared")
	g.P("   * command/event must be implemented — a missing handler is a compile error. */")
	g.P("  public interface ", s.GoName, "Handler {")
	for _, m := range e.methods(s) {
		g.P("    ", m.results, " ", m.name, "(", m.params, ")", m.throws, ";")
	}
	g.P("  }")
	g.P()
}

// methodSig is one handler method, shared by the interface and (future) scaffold.
type javaMethod struct {
	name    string
	params  string
	results string // "void" or a type
	throws  string // " throws Exception" or ""
}

func (e javaEmitter) methods(s *Service) []javaMethod {
	switch s.Component.Kind {
	case KindAggregate:
		return e.aggregateMethods(s)
	case KindSaga:
		return e.sagaMethods(s)
	case KindProjector:
		return e.projectorMethods(s)
	case KindProcessManager:
		return e.pmMethods(s)
	}
	return nil
}

func (e javaEmitter) aggregateMethods(s *Service) []javaMethod {
	state := javaType(s.State) + ".Builder"
	var out []javaMethod
	for _, h := range s.Handlers {
		results := jEventBook
		if h.TypedEmit() {
			results = jList + "<" + javaType(h.Emits[0]) + ">"
		}
		out = append(out, javaMethod{
			name:    lowerFirst(h.MethodName),
			params:  javaType(h.Message) + " cmd, " + state + " state, " + jCctx + " cctx",
			results: results,
			throws:  " throws Exception",
		})
	}
	for _, a := range s.Appliers {
		out = append(out, javaMethod{
			name:    lowerFirst(a.MethodName),
			params:  state + " state, " + javaType(a.Message) + " event",
			results: "void",
		})
	}
	for _, r := range s.Rejections {
		out = append(out, javaMethod{
			name:    lowerFirst(r.MethodName),
			params:  jNotification + " n, " + jRejNotif + " rejection, " + state + " state, " + jCctx + " cctx",
			results: jBusinessResp,
			throws:  " throws Exception",
		})
	}
	return out
}

func (e javaEmitter) sagaMethods(s *Service) []javaMethod {
	var out []javaMethod
	for _, h := range s.Handlers {
		out = append(out, javaMethod{
			name:    lowerFirst(h.MethodName),
			params:  javaType(h.Message) + " event, " + jDestinations + " dests",
			results: jSagaEmission,
			throws:  " throws Exception",
		})
	}
	for _, r := range s.Rejections {
		out = append(out, javaMethod{
			name:    lowerFirst(r.MethodName),
			params:  jNotification + " n, " + jRejNotif + " rejection",
			results: jList + "<" + jEventBook + ">",
			throws:  " throws Exception",
		})
	}
	return out
}

func (e javaEmitter) projectorMethods(s *Service) []javaMethod {
	state := javaType(s.State) + ".Builder"
	var out []javaMethod
	for _, h := range s.Handlers {
		out = append(out, javaMethod{
			name:    lowerFirst(h.MethodName),
			params:  state + " projection, " + javaType(h.Message) + " event",
			results: "void",
			throws:  " throws Exception",
		})
	}
	out = append(out, javaMethod{
		name:    "finish",
		params:  state + " projection, " + jEventBook + " events",
		results: jProjection,
		throws:  " throws Exception",
	})
	return out
}

