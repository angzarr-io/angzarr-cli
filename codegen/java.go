// --- wiring (dispatch table population) ---------------------------------------

func (e javaEmitter) emitAggregate(g *protogen.GeneratedFile, s *Service) error {
	e.emitInterface(g, s, s.GoName+" aggregate")
	state := javaType(s.State) + ".Builder"
	g.P("  /** Populates the aggregate table from the proto declaration. */")
	g.P("  public static ", jAggDispatch, " new", s.GoName, "Dispatch(", s.GoName, "Handler h) {")
	g.P("    ", jRebuilder, " rebuilder = new ", jRebuilder, "(", javaType(s.State), "::newBuilder);")
	g.P("    rebuilder.withSnapshot((state, payload) -> ((", state, ") state).mergeFrom(payload.getValue()));")
	for _, a := range s.Appliers {
		g.P("    rebuilder.apply(", quote(a.fqType()), ", (state, payload) ->")
		g.P("        h.", lowerFirst(a.MethodName), "((", state, ") state, ", javaType(a.Message), ".parseFrom(payload.getValue())));")
	}
	g.P("    return new ", jAggDispatch, "(", quote(s.GoName), ", ", quote(s.Component.InputDomain), ", rebuilder)")
	for _, h := range s.Handlers {
		g.P("        .onCommand(", quote(string(h.Message.Desc.FullName())), ", (cmdAny, state, cctx) -> {")
		g.P("          ", javaType(h.Message), " cmd = ", parseAny(h.Message, "cmdAny"), ";")
		if h.TypedEmit() {
			g.P("          ", jList, "<", javaType(h.Emits[0]), "> events = h.", lowerFirst(h.MethodName), "(cmd, (", state, ") state, cctx);")
			g.P("          ", jEventBook, ".Builder book = ", jEventBook, ".newBuilder();")
			g.P("          for (", javaType(h.Emits[0]), " ev : events) {")
			g.P("            book.addPages(", jEventPage, ".newBuilder().setEvent(", jPack, ".pack(ev)));")
			g.P("          }")
			g.P("          return book.build();")
		} else {
			g.P("          return h.", lowerFirst(h.MethodName), "(cmd, (", state, ") state, cctx);")
		}
		g.P("        })")
	}
	for _, r := range s.Rejections {
		g.P("        .onRejected(", quote(r.Command), ", (n, rejection, state, cctx) ->")
		g.P("            h.", lowerFirst(r.MethodName), "(n, rejection, (", state, ") state, cctx))")
	}
	g.P("        ;")
	g.P("  }")
	g.P()
	e.emitRegister(g, s, "registerAggregate")
	return nil
}

func (e javaEmitter) emitSaga(g *protogen.GeneratedFile, s *Service) error {
	e.emitInterface(g, s, s.GoName+" saga")
	g.P("  /** Populates the saga table from the proto declaration. */")
	g.P("  public static ", jSagaDispatch, " new", s.GoName, "Dispatch(", s.GoName, "Handler h) {")
	g.P("    return new ", jSagaDispatch, "(", quote(s.GoName), ", ", quote(s.Component.InputDomain), ", ", jList, ".of(", quote(s.Component.OutputDomain), "))")
	for _, h := range s.Handlers {
		g.P("        .onEvent(", quote(string(h.Message.Desc.FullName())), ", (eventAny, dests) -> {")
		g.P("          ", javaType(h.Message), " event = ", parseAny(h.Message, "eventAny"), ";")
		g.P("          return h.", lowerFirst(h.MethodName), "(event, dests);")
		g.P("        })")
	}
	for _, r := range s.Rejections {
		g.P("        .onRejected(", quote(r.Command), ", (n, rejection) -> h.", lowerFirst(r.MethodName), "(n, rejection))")
	}
	g.P("        ;")
	g.P("  }")
	g.P()
	e.emitRegister(g, s, "registerSaga")
	return nil
}

func (e javaEmitter) emitProjector(g *protogen.GeneratedFile, s *Service) error {
	e.emitInterface(g, s, s.GoName+" projector")
	state := javaType(s.State) + ".Builder"
	g.P("  /** Populates the projector table from the proto declaration. */")
	g.P("  public static ", jProjDispatch, " new", s.GoName, "Dispatch(", s.GoName, "Handler h) {")
	g.P("    return new ", jProjDispatch, "(", quote(s.GoName), ", ", javaType(s.State), "::newBuilder)")
	g.P("        .forDomains(", quote(s.Component.InputDomain), ")")
	for _, h := range s.Handlers {
		g.P("        .onEvent(", quote(string(h.Message.Desc.FullName())), ", (projection, eventAny) -> {")
		g.P("          ", javaType(h.Message), " event = ", parseAny(h.Message, "eventAny"), ";")
		g.P("          h.", lowerFirst(h.MethodName), "((", state, ") projection, event);")
		g.P("        })")
	}
	g.P("        .finish((projection, events) -> h.finish((", state, ") projection, events));")
	g.P("  }")
	g.P()
	e.emitRegister(g, s, "registerProjector")
	return nil
}

