func (goEmitter) emitProjector(g *protogen.GeneratedFile, s *Service) error {
	name := s.Proto.GoName
	component := s.Component
	statePtr := "*" + g.QualifiedGoIdent(s.State.GoIdent)
	evtBook := "*" + ident(g, angzarrPb, "EventBook")
	projection := "*" + ident(g, angzarrPb, "Projection")

	g.P("// ", name, "Handler is the strict business seam for the ", name, " projector.")
	g.P("type ", name, "Handler interface {")
	for _, h := range s.Handlers {
		g.P("\t", h.Method.GoName, "(projection ", statePtr, ", event *", g.QualifiedGoIdent(h.Method.Input.GoIdent), ") error")
	}
	g.P("\t// Finish packs the folded instance into the wire Projection.")
	g.P("\tFinish(projection ", statePtr, ", events ", evtBook, ") (", projection, ", error)")
	g.P("}")
	g.P()
	g.P("// New", name, "Dispatch populates the engine projector table from the proto declaration.")
	g.P("func New", name, "Dispatch(h ", name, "Handler) *", ident(g, angzarrPkg, "ProjectorDispatch"), "[", statePtr, "] {")
	g.P("\treturn ", ident(g, angzarrPkg, "NewProjectorDispatch"), "(", quote(string(s.Proto.Desc.Name())), ", func() ", statePtr, " { return &", g.QualifiedGoIdent(s.State.GoIdent), "{} }).")
	g.P("\t\tForDomains(", quote(component.InputDomain), ").")
	for _, h := range s.Handlers {
		g.P("\t\tOnEvent(", quote(string(h.Method.Input.Desc.FullName())), ", func(projection ", statePtr, ", eventAny *", ident(g, anypbPkg, "Any"), ") error {")
		g.P("\t\t\tevent := &", g.QualifiedGoIdent(h.Method.Input.GoIdent), "{}")
		g.P("\t\t\tif err := eventAny.UnmarshalTo(event); err != nil { return ", ident(g, angzarrPkg, "AnyDecodeError"), "(eventAny.TypeUrl, err) }")
		g.P("\t\t\treturn h.", h.Method.GoName, "(projection, event)")
		g.P("\t\t}).")
	}
	g.P("\t\tFinish(h.Finish)")
	g.P("}")
	g.P()
	return nil
}

func quote(s string) string { return fmt.Sprintf("%q", s) }

// trailer continues the fluent chain when more registrations follow.
func trailer(i, n int, more bool) string {
	if i < n-1 || more {
		return "." // not last — keep chaining
	}
	return ""
}
