// emitCppAppliers registers each event applier on the rebuilder. Identical for
// aggregates and process managers (both rebuild their own state).
func emitCppAppliers(g *protogen.GeneratedFile, s *Service, state string) {
	for _, a := range s.Appliers {
		g.P("  rebuilder.Apply(", cppQuote(fqName(a.Message)), ", [&h](", state, "& state, const ", cppAny, "& payload) {")
		g.P("    h.", a.MethodName, "(state, ", cppParse(a.Message, "payload"), ");")
		g.P("  });")
	}
}

func (e cppEmitter) emitRegister(g *protogen.GeneratedFile, s *Service, routerMethod string) {
	g.P("// Registers a ", s.GoName, "Handler with the router.")
	g.P("inline void Register", s.GoName, "(", cppRouter, "& r, ", s.GoName, "Handler& h) {")
	g.P("  r.", routerMethod, "(New", s.GoName, "Dispatch(h));")
	g.P("}")
	g.P()
}

// EmitScaffold writes the generate-once developer stub: one struct per component
// implementing the Handler, with TODO bodies.
func (e cppEmitter) EmitScaffoldComponent(g *protogen.GeneratedFile, file *protogen.File, s *Service) error {
	g.P("// Scaffolded ONCE by angzarr codegen cpp — this file is YOURS.")
	g.P("// Regeneration will NOT overwrite it; keep the generated Handler interface")
	g.P("// implemented as commands/events are added to the proto.")
	g.P("#pragma once")
	g.P()
	g.P(`#include "`, e.componentInclude(file, s), `"`)
	g.P()
	g.P("namespace ", cppNamespace(file), " {")
	g.P()
	g.P("// Implement ", s.GoName, "Handler here.")
	g.P("class ", s.GoName, " : public ", s.GoName, "Handler {")
	g.P(" public:")
	for _, m := range e.methods(s) {
		g.P("  ", m.results, " ", m.name, "(", m.params, ") override {")
		if m.results == "void" {
			g.P("    // TODO: implement ", s.GoName, "::", m.name)
		} else {
			g.P("    throw std::runtime_error(", cppQuote("TODO: implement "+s.GoName+"::"+m.name), ");")
		}
		g.P("  }")
	}
	g.P("};")
	g.P()
	g.P("}  // namespace ", cppNamespace(file))
	return nil
}

// --- C++ naming helpers -------------------------------------------------------

// cppParse renders the parse of an Any payload into its typed message, routing a
// decode failure through CodedError as ANY_DECODE_FAILED.
func cppParse(m *protogen.Message, anyVar string) string {
	return cppCoded + "::Parse<" + cppType(m) + ">(" + anyVar + ")"
}

func cppGeneric(base, typeArg string) string {
	return base + "<" + typeArg + ">"
}

// cppQuote renders a Go string as a C++ string literal.
func cppQuote(s string) string {
	return `"` + s + `"`
}

func cppNamespace(file *protogen.File) string {
	return cppNamespaceOf(file.Desc)
}

func cppNamespaceOf(fd protoreflect.FileDescriptor) string {
	return strings.ReplaceAll(string(fd.Package()), ".", "::")
}

// cppType computes the fully-qualified C++ type name of a message: the file's
// namespace (package dots→"::") then the nested message path ("::" joined;
// protobuf-cpp exposes nested messages as Outer::Inner).
func cppType(m *protogen.Message) string {
	ns := cppNamespaceOf(m.Desc.ParentFile())
	nested := cppNestedName(m.Desc)
	if ns == "" {
		return nested
	}
	return ns + "::" + nested
}

func cppNestedName(md protoreflect.MessageDescriptor) string {
	parts := []string{string(md.Name())}
	for {
		parent, ok := md.Parent().(protoreflect.MessageDescriptor)
		if !ok {
			break
		}
		parts = append([]string{string(parent.Name())}, parts...)
		md = parent
	}
	return strings.Join(parts, "::")
}

// cppInclude is the generated protobuf header for a proto file (path.proto → path.pb.h).
func cppInclude(fd protoreflect.FileDescriptor) string {
	return strings.TrimSuffix(fd.Path(), ".proto") + ".pb.h"
}

// componentInclude is the gen-relative path of a component's wiring header
// (beside the proto), used by the scaffold to include the interface it stubs.
func (e cppEmitter) componentInclude(file *protogen.File, s *Service) string {
	dir := ""
	if i := strings.LastIndex(file.Desc.Path(), "/"); i >= 0 {
		dir = file.Desc.Path()[:i+1]
	}
	return dir + snake(s.GoName) + "_angzarr.h"
}

// includes collects the .pb.h headers the wiring needs: every referenced
// message's file (state, commands/events, emitted events), sorted + deduped.
func (e cppEmitter) includes(file *protogen.File, services []*Service) []string {
	set := map[string]struct{}{cppInclude(file.Desc): {}}
	add := func(m *protogen.Message) {
		if m != nil {
			set[cppInclude(m.Desc.ParentFile())] = struct{}{}
		}
	}
	for _, s := range services {
		add(s.State)
		for _, h := range s.Handlers {
			add(h.Message)
			for _, em := range h.Emits {
				add(em)
			}
		}
		for _, a := range s.Appliers {
			add(a.Message)
		}
	}
	out := make([]string, 0, len(set))
	for inc := range set {
		out = append(out, inc)
	}
	sort.Strings(out)
	return out
}
