func (e csharpEmitter) emitRegister(g *protogen.GeneratedFile, s *Service, routerMethod string) {
	g.P("    /// <summary>Registers a ", s.GoName, "Handler with the router.</summary>")
	g.P("    public static void Register", s.GoName, "(", csRouter, " r, ", s.GoName, "Handler h)")
	g.P("    {")
	g.P("        r.", routerMethod, "(New", s.GoName, "Dispatch(h));")
	g.P("    }")
	g.P()
}

// EmitScaffold writes the generate-once developer stub: one outer static class
// per proto file with a public sealed <Component> stub implementing the Handler.
func (e csharpEmitter) EmitScaffold(g *protogen.GeneratedFile, file *protogen.File, services []*Service) error {
	g.P("// Scaffolded ONCE by angzarr codegen csharp — this file is YOURS.")
	g.P("// Regeneration will NOT overwrite it; keep the generated Handler interfaces")
	g.P("// implemented as commands/events are added to the proto.")
	g.P()
	g.P("namespace ", csNamespace(file), ";")
	g.P()
	g.P("public static class ", csScaffoldClass(file))
	g.P("{")
	for _, s := range services {
		g.P("    /// <summary>Implement ", s.GoName, "Handler here.</summary>")
		g.P("    public sealed class ", s.GoName, " : ", csWiringClass(file), ".", s.GoName, "Handler")
		g.P("    {")
		for _, m := range e.methods(s) {
			g.P("        public ", m.results, " ", m.name, "(", m.params, ")")
			g.P("        {")
			if m.results == "void" {
				g.P("            // TODO: implement ", s.GoName, ".", m.name)
			} else {
				g.P("            throw new System.NotImplementedException(", quote("TODO: implement "+s.GoName+"."+m.name), ");")
			}
			g.P("        }")
		}
		g.P("    }")
		g.P()
	}
	g.P("}")
	return nil
}

// --- C# naming helpers --------------------------------------------------------

// csParseAny renders the parse of an Any payload into the typed message, routing
// a decode failure through CodedError as ANY_DECODE_FAILED.
func csParseAny(m *protogen.Message, anyVar string) string {
	return csCoded + ".Parse(" + csType(m) + ".Parser, " + anyVar + ")"
}

// csParse parses a typed message directly from a ByteString (applier payloads).
func csParse(m *protogen.Message, bytesVar string) string {
	return csType(m) + ".Parser.ParseFrom(" + bytesVar + ")"
}

func csWiringClass(file *protogen.File) string {
	return protoBase(file) + "_angzarr"
}

func csScaffoldClass(file *protogen.File) string {
	return protoBase(file) + "_angzarr_handler"
}

// csNamespace resolves a file's C# namespace: the csharp_namespace option, else
// each proto package segment PascalCased and joined with ".".
func csNamespace(file *protogen.File) string {
	return csNamespaceOf(file.Desc)
}

func csNamespaceOf(fd protoreflect.FileDescriptor) string {
	if opts, ok := fd.Options().(*descriptorpb.FileOptions); ok && opts.CsharpNamespace != nil {
		return opts.GetCsharpNamespace()
	}
	return pascalNamespace(string(fd.Package()))
}

// pascalNamespace PascalCases each dotted package segment (test.counter →
// Test.Counter), matching protoc's default C# namespace derivation.
func pascalNamespace(pkg string) string {
	if pkg == "" {
		return ""
	}
	parts := strings.Split(pkg, ".")
	for i, p := range parts {
		parts[i] = snakeToPascal(p)
	}
	return strings.Join(parts, ".")
}

// csType computes the fully-qualified C# type name of a message: the file's C#
// namespace, then the nested message path joined by ".Types." (Google.Protobuf
// nests message types under a Types container).
func csType(m *protogen.Message) string {
	fd := m.Desc.ParentFile()
	ns := csNamespaceOf(fd)
	nested := csNestedName(m.Desc)
	if ns == "" {
		return nested
	}
	return ns + "." + nested
}

// csNestedName joins a message's name path within its file, with ".Types."
// between levels (Outer.Types.Inner).
func csNestedName(md protoreflect.MessageDescriptor) string {
	parts := []string{string(md.Name())}
	for {
		parent, ok := md.Parent().(protoreflect.MessageDescriptor)
		if !ok {
			break
		}
		parts = append([]string{string(parent.Name())}, parts...)
		md = parent
	}
	return strings.Join(parts, ".Types.")
}
