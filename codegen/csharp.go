package codegen

// C# emitter. Like the Java emitter, each proto file's components are emitted
// into ONE wiring file (the conformance fixtures, a separate namespace, import
// the handler types) as a single public static outer class (named
// <protofile>_angzarr) holding a public nested <Component>Handler interface, a
// New<Component>Dispatch factory, and a Register<Component> convenience per
// component. Type references are fully qualified (no using management):
// framework message types under the Angzarr namespace, runtime types under
// Angzarr.Router, and domain message types computed from the file's
// csharp_namespace (or the PascalCase package). Unlike Java, Google.Protobuf
// messages are MUTABLE — state is the message itself (no Builder), the factory
// is () => new <State>(), and appliers fold via property setters / MergeFrom.

import (
	"fmt"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/reflect/protoreflect"
	"google.golang.org/protobuf/types/descriptorpb"
)

// Runtime (binding) + framework (message) type names, fully qualified.
const (
	csRouter       = "Angzarr.Router.Router"
	csAggDispatch  = "Angzarr.Router.AggregateDispatch"
	csSagaDispatch = "Angzarr.Router.SagaDispatch"
	csProjDispatch = "Angzarr.Router.ProjectorDispatch"
	csPmDispatch   = "Angzarr.Router.ProcessManagerDispatch"
	csRebuilder    = "Angzarr.Router.Rebuilder"
	csCctx         = "Angzarr.Router.CommandContext"
	csDestinations = "Angzarr.Router.Destinations"
	csPack         = "Angzarr.Router.Pack"
	csCoded        = "Angzarr.Router.CodedError"
	csSagaEmission = "Angzarr.Router.SagaEmission"
	csPmRejection  = "Angzarr.Router.PmRejection"
	csEventBook    = "Angzarr.EventBook"
	csEventPage    = "Angzarr.EventPage"
	csBusinessResp = "Angzarr.BusinessResponse"
	csNotification = "Angzarr.Notification"
	csRejNotif     = "Angzarr.RejectionNotification"
	csProjection   = "Angzarr.Projection"
	csPmResponse   = "Angzarr.ProcessManagerHandleResponse"
	csList         = "System.Collections.Generic.IReadOnlyList"
)

type csharpEmitter struct{}

func (csharpEmitter) Lang() string           { return "csharp" }
func (csharpEmitter) Suffix() string         { return "_angzarr.cs" }
func (csharpEmitter) ScaffoldSuffix() string { return "_angzarr_handler.cs" }

func (e csharpEmitter) EmitFile(g *protogen.GeneratedFile, file *protogen.File, services []*Service) error {
	g.P("// <auto-generated/>")
	g.P("// Code generated by angzarr codegen csharp. DO NOT EDIT.")
	g.P("// source: ", file.Desc.Path())
	g.P()
	g.P("namespace ", csNamespace(file), ";")
	g.P()
	g.P("public static class ", csWiringClass(file))
	g.P("{")
	for _, s := range services {
		var err error
		switch s.Component.Kind {
		case KindAggregate:
			err = e.emitAggregate(g, s)
		case KindSaga:
			err = e.emitSaga(g, s)
		case KindProjector:
			err = e.emitProjector(g, s)
		case KindProcessManager:
			err = e.emitPM(g, s)
		default:
			err = fmt.Errorf("unsupported component kind %v", s.Component.Kind)
		}
		if err != nil {
			return err
		}
	}
	g.P("}")
	return nil
}

// --- interface (the strict seam) ----------------------------------------------

func (e csharpEmitter) emitInterface(g *protogen.GeneratedFile, s *Service, doc string) {
	g.P("    /// <summary>The strict business seam for the ", doc, ". Every declared")
	g.P("    /// command/event must be implemented — a missing handler is a compile error.</summary>")
	g.P("    public interface ", s.GoName, "Handler")
	g.P("    {")
	for _, m := range e.methods(s) {
		g.P("        ", m.results, " ", m.name, "(", m.params, ");")
	}
	g.P("    }")
	g.P()
}

// csMethod is one handler method, shared by the interface and the scaffold.
type csMethod struct {
	name    string
	params  string
	results string // "void" or a type
}

func (e csharpEmitter) methods(s *Service) []csMethod {
	switch s.Component.Kind {
	case KindAggregate:
		return e.aggregateMethods(s)
	case KindSaga:
		return e.sagaMethods(s)
	case KindProjector:
		return e.projectorMethods(s)
	case KindProcessManager:
		return e.pmMethods(s)
	}
	return nil
}

func (e csharpEmitter) aggregateMethods(s *Service) []csMethod {
	state := csType(s.State)
	var out []csMethod
	for _, h := range s.Handlers {
		results := csEventBook
		if h.TypedEmit() {
			results = csList + "<" + csType(h.Emits[0]) + ">"
		}
		out = append(out, csMethod{
			name:    h.MethodName,
			params:  csType(h.Message) + " cmd, " + state + " state, " + csCctx + " cctx",
			results: results,
		})
	}
	for _, a := range s.Appliers {
		out = append(out, csMethod{
			name:    a.MethodName,
			params:  state + " state, " + csType(a.Message) + " ev",
			results: "void",
		})
	}
	for _, r := range s.Rejections {
		out = append(out, csMethod{
			name:    r.MethodName,
			params:  csNotification + " n, " + csRejNotif + " rejection, " + state + " state, " + csCctx + " cctx",
			results: csBusinessResp,
		})
	}
	return out
}

func (e csharpEmitter) sagaMethods(s *Service) []csMethod {
	var out []csMethod
	for _, h := range s.Handlers {
		out = append(out, csMethod{
			name:    h.MethodName,
			params:  csType(h.Message) + " ev, " + csDestinations + " dests",
			results: csSagaEmission,
		})
	}
	for _, r := range s.Rejections {
		out = append(out, csMethod{
			name:    r.MethodName,
			params:  csNotification + " n, " + csRejNotif + " rejection",
			results: csList + "<" + csEventBook + ">",
		})
	}
	return out
}

func (e csharpEmitter) projectorMethods(s *Service) []csMethod {
	state := csType(s.State)
	var out []csMethod
	for _, h := range s.Handlers {
		out = append(out, csMethod{
			name:    h.MethodName,
			params:  state + " projection, " + csType(h.Message) + " ev",
			results: "void",
		})
	}
	out = append(out, csMethod{
		name:    "Finish",
		params:  state + " projection, " + csEventBook + " events",
		results: csProjection,
	})
	return out
}

func (e csharpEmitter) pmMethods(s *Service) []csMethod {
	state := csType(s.State)
	var out []csMethod
	for _, h := range s.Handlers {
		out = append(out, csMethod{
			name:    h.MethodName,
			params:  csType(h.Message) + " ev, " + state + " state, " + csDestinations + " dests",
			results: csPmResponse,
		})
	}
	for _, a := range s.Appliers {
		out = append(out, csMethod{
			name:    a.MethodName,
			params:  state + " state, " + csType(a.Message) + " ev",
			results: "void",
		})
	}
	for _, r := range s.Rejections {
		out = append(out, csMethod{
			name:    r.MethodName,
			params:  csNotification + " n, " + csRejNotif + " rejection, " + state + " state",
			results: csPmRejection,
		})
	}
	return out
}

// --- wiring (dispatch table population) ---------------------------------------

func (e csharpEmitter) emitAggregate(g *protogen.GeneratedFile, s *Service) error {
	e.emitInterface(g, s, s.GoName+" aggregate")
	state := csType(s.State)
	g.P("    /// <summary>Populates the aggregate table from the proto declaration.</summary>")
	g.P("    public static ", csAggDispatch, " New", s.GoName, "Dispatch(", s.GoName, "Handler h)")
	g.P("    {")
	g.P("        var rebuilder = new ", csRebuilder, "(() => new ", state, "());")
	g.P("        rebuilder.WithSnapshot((state, payload) => ((", state, ")state).MergeFrom(payload.Value));")
	for _, a := range s.Appliers {
		g.P("        rebuilder.Apply(", quote(a.fqType()), ", (state, payload) =>")
		g.P("            h.", a.MethodName, "((", state, ")state, ", csParse(a.Message, "payload.Value"), "));")
	}
	g.P("        return new ", csAggDispatch, "(", quote(s.GoName), ", ", quote(s.Component.InputDomain), ", rebuilder)")
	for _, h := range s.Handlers {
		g.P("            .OnCommand(", quote(string(h.Message.Desc.FullName())), ", (cmdAny, state, cctx) =>")
		g.P("            {")
		g.P("                ", csType(h.Message), " cmd = ", csParseAny(h.Message, "cmdAny"), ";")
		if h.TypedEmit() {
			g.P("                var events = h.", h.MethodName, "(cmd, (", state, ")state, cctx);")
			g.P("                var book = new ", csEventBook, "();")
			g.P("                foreach (var ev in events)")
			g.P("                {")
			g.P("                    book.Pages.Add(new ", csEventPage, " { Event = ", csPack, ".Pack(ev) });")
			g.P("                }")
			g.P("                return book;")
		} else {
			g.P("                return h.", h.MethodName, "(cmd, (", state, ")state, cctx);")
		}
		g.P("            })")
	}
	for _, r := range s.Rejections {
		g.P("            .OnRejected(", quote(r.Command), ", (n, rejection, state, cctx) =>")
		g.P("                h.", r.MethodName, "(n, rejection, (", state, ")state, cctx))")
	}
	g.P("            ;")
	g.P("    }")
	g.P()
	e.emitRegister(g, s, "RegisterAggregate")
	return nil
}

func (e csharpEmitter) emitSaga(g *protogen.GeneratedFile, s *Service) error {
	e.emitInterface(g, s, s.GoName+" saga")
	g.P("    /// <summary>Populates the saga table from the proto declaration.</summary>")
	g.P("    public static ", csSagaDispatch, " New", s.GoName, "Dispatch(", s.GoName, "Handler h)")
	g.P("    {")
	g.P("        return new ", csSagaDispatch, "(", quote(s.GoName), ", ", quote(s.Component.InputDomain), ", ", quote(s.Component.OutputDomain), ")")
	for _, h := range s.Handlers {
		g.P("            .OnEvent(", quote(string(h.Message.Desc.FullName())), ", (eventAny, dests) =>")
		g.P("            {")
		g.P("                ", csType(h.Message), " ev = ", csParseAny(h.Message, "eventAny"), ";")
		g.P("                return h.", h.MethodName, "(ev, dests);")
		g.P("            })")
	}
	for _, r := range s.Rejections {
		g.P("            .OnRejected(", quote(r.Command), ", (n, rejection) => h.", r.MethodName, "(n, rejection))")
	}
	g.P("            ;")
	g.P("    }")
	g.P()
	e.emitRegister(g, s, "RegisterSaga")
	return nil
}

func (e csharpEmitter) emitProjector(g *protogen.GeneratedFile, s *Service) error {
	e.emitInterface(g, s, s.GoName+" projector")
	state := csType(s.State)
	g.P("    /// <summary>Populates the projector table from the proto declaration.</summary>")
	g.P("    public static ", csProjDispatch, " New", s.GoName, "Dispatch(", s.GoName, "Handler h)")
	g.P("    {")
	g.P("        return new ", csProjDispatch, "(", quote(s.GoName), ", () => new ", state, "())")
	g.P("            .ForDomains(", quote(s.Component.InputDomain), ")")
	for _, h := range s.Handlers {
		g.P("            .OnEvent(", quote(string(h.Message.Desc.FullName())), ", (projection, eventAny) =>")
		g.P("            {")
		g.P("                ", csType(h.Message), " ev = ", csParseAny(h.Message, "eventAny"), ";")
		g.P("                h.", h.MethodName, "((", state, ")projection, ev);")
		g.P("            })")
	}
	g.P("            .Finish((projection, events) => h.Finish((", state, ")projection, events));")
	g.P("    }")
	g.P()
	e.emitRegister(g, s, "RegisterProjector")
	return nil
}

